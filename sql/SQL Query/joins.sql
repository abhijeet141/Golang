CREATE TABLE Bonus(
	WORKER_REF_ID INT,
    WORKER_TITLE VARCHAR(255),
    AFFECTED_FROM DATETIME,
    FOREIGN KEY(WORKER_REF_ID) 
			REFERENCES Worker(Worker_ID)
            ON DELETE CASCADE
);

