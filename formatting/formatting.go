package main

import "fmt"

func main() {
	fmt.Printf("%v \n", 123)
	fmt.Printf("%d \n", 123)
	fmt.Printf("%b \n", 123)
	fmt.Printf("%x \n", 123)
	fmt.Printf("%X \n", 123)
	fmt.Printf("%T \n", 123)
	s := "hello"
	fmt.Printf("%s \n", s)
	fmt.Printf("%q \n", s)
	fmt.Printf("%v \n", s)

}
